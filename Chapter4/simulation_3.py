# 왕실의 나이트 (115p)

pos = input()
cnt = 0

x = ord(pos[0]) - 97 + 1  # 소문자 a의 아스키토드 = 97 // ord() : 아스키코드로 변환
y = int(pos[1])

move = [(-2, -1), (-2, 1), (2, -1), (2, 1), (-1, -2), (1, -2), (-1, 2), (1, 2)]
    
for i in move:
    if (x + i[0] >= 1) & (x + i[0] <= 8):
        if (y + i[1] >= 1) & (y + i[1] <= 8):
            cnt += 1    
    
print(cnt)